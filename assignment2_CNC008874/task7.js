var firstNo = prompt("Enter first number");
var secondNo = prompt("Enter second number");
var operation = prompt("choose Operation: +, -, *, /, %")
if (operation == "+")
{
    alert(firstNo + secondNo)
}
else if (operation == "-")
{
    alert(firstNo - secondNo)
}
else if (operation == "*")
{
    alert(firstNo * secondNo)
}
else if (operation == "/")
{
    alert(firstNo / secondNo)
}
else if (operation == "%")
{
    alert(firstNo % secondNo)
}